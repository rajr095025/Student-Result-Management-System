    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        try{
            //creating a new instance of the com. mysql. jdbc. Driver class and Register the driver.
            Class.forName("com.mysql.jdbc.Driver");
            //connecting to database with port number 3306
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Rakesh123@");
            //creating statement and connecting to database srm
            //adding data to the student table
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student inner join result where student.rollno = '"+rollno+"' and result.rollno = '"+rollno+"'");
            if(rs.next()){
                setVisible(false);
                new studentIndex(rollno).setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "Roll number is incorrect");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

