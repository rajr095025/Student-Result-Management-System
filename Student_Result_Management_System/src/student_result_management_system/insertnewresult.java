    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        //getting input from user and storing in database srm in table result
        String rollno = jTextField1.getText();
        String maths = jTextField2.getText();
        String computerscience = jTextField3.getText();
        String physics = jTextField4.getText();
        String chemistry = jTextField5.getText();
        String english = jTextField6.getText();
        String socialscience = jTextField7.getText();
        
        try{
            //creating a new instance of the com. mysql. jdbc. Driver class and Register the driver.
            Class.forName("com.mysql.jdbc.Driver");
            //connecting to database with port number 3306
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Rakesh123@");
            
            //creating statement and connecting to database srm
            //adding data to the student table
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno='"+rollno+"'");
            
            if(rs.next()){
                st.executeUpdate("insert into result (rollno,maths,computerscience,physics,chemistry,english,socialscience) values('"+rollno+"','"+maths+"','"+computerscience+"','"+physics+"','"+chemistry+"','"+english+"','"+socialscience+"')");
                JOptionPane.showMessageDialog(null,"Successfully added the data.");
                setVisible(false);
                new insertnewresult().setVisible(true);
            }
            
            else{
                JOptionPane.showMessageDialog(null,"This roll number doesnot exist.");
            }
        }
        
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
        
    }//GEN-LAST:event_jButton6ActionPerformed

